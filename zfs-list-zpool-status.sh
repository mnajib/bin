#!/usr/bin/env bash

function aa {
  watch '
  echo "----------------------------------------------------------------------------------"; \
  echo " zpool list -v"; \
  echo "----------------------------------------------------------------------------------"; \
  zpool list -v ; \
  echo ""; \
  echo "----------------------------------------------------------------------------------"; \
  echo " zpool status -v"; \
  echo "----------------------------------------------------------------------------------"; \
  zpool status -v; \
  echo "----------------------------------------------------------------------------------"; \
  '
}

function aa2 {
  echo "----------------------------------------------------------------------------------"
  echo " zpool list -v"
  echo "----------------------------------------------------------------------------------"
  zpool list -v
  echo ""
  echo "----------------------------------------------------------------------------------"
  echo " zpool status -v"
  echo "----------------------------------------------------------------------------------"
  zpool status -v
  echo "----------------------------------------------------------------------------------"
}

aa2
